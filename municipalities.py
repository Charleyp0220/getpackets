_cache = None


def load_municipalities() -> list[dict]:
    global _cache
    if _cache is not None:
        return _cache

    places = []
    seen   = set()
    for (name, state, place_type, pop) in _RAW:
        if pop < MIN_POPULATION:
            continue
        key = (name.lower(), state)
        if key in seen:
            continue
        seen.add(key)
        places.append({
            "name":       name,
            "state":      state,
            "state_abbr": STATE_ABBREVS.get(state, ""),
            "place_type": place_type,
            "population": pop,
            "county":     "",
        })

    # Sort by population, cap at MAX_PLACES
    places.sort(key=lambda x: x["population"], reverse=True)
    _cache = places[:MAX_PLACES]
    return _cache


def get_places_for_state(state_name: str) -> list[dict]:
    return [p for p in load_municipalities() if p["state"] == state_name]


def get_all_states() -> list[str]:
    return sorted(set(p["state"] for p in load_municipalities()))

